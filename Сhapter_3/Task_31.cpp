#include <iostream>
using namespace std;

int main() {
    setlocale(LC_ALL, "RU");
    int n;

    // Ввод натурального числа
    cout << "Введите натуральное число (n > 999): ";
    cin >> n;

    // Проверка, что число действительно натуральное и больше 999
    if (n <= 999) {
        cout << "Ошибка! Число должно быть больше 999\n";
        return 1;
    }

    // а) Число сотен - третья с конца цифра числа
    int hundreds = (n / 100) % 10;

    // б) Число тысяч - четвертая с конца цифра числа
    int thousands = (n / 1000) % 10;

    // Вывод результатов
    cout << "Исходное число: " << n << endl;
    cout << "а) Число сотен: " << hundreds << endl;
    cout << "б) Число тысяч: " << thousands << endl;

    return 0;
}