#include <iostream>
using namespace std;

int main() {
    setlocale(LC_ALL, "RU");
    int a3, a2, a1, b2, b1;

    cout << "Введите цифры трехзначного числа (сотни a3, десятки a2, единицы a1): ";
    cin >> a3 >> a2 >> a1;

    cout << "Введите цифры двузначного числа (десятки b2, единицы b1): ";
    cin >> b2 >> b1;

    // Вычисляем цифры результата в одну строку
    int c1 = (a1 + b1) % 10;                                          // единицы
    int c2 = (a2 + b2 + (a1 + b1) / 10) % 10;                        // десятки
    int c3 = a3 + (a2 + b2 + (a1 + b1) / 10) / 10;                   // сотни

    cout << "\nСотни: " << c3 << ", Десятки: " << c2 << ", Единицы: " << c1 << endl;

    return 0;
}