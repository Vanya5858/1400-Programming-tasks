#include <iostream>
using namespace std;

int main() {
    setlocale(LC_ALL, "RU");
    int n;

    cout << "Введите число n (100 <= n <= 999): ";
    cin >> n;

    if (n < 100 || n > 999) {
        cout << "Ошибка! Число n должно быть в диапазоне 100 ≤ n ≤ 999\n";
        return 1;
    }

    // Разбираем число n на составляющие
    int a = n / 100;           // первая цифра исходного числа x
    int remainder = n % 100;   // оставшаяся часть (10c + b)
    int c = remainder / 10;    // третья цифра исходного числа x
    int b = remainder % 10;    // вторая цифра исходного числа x

    // Восстанавливаем исходное число x
    int x = 100 * a + 10 * b + c;

    // Вывод результатов
    cout << "Для n = " << n << " найдено число x = " << x << endl;
    cout << "Проверка:\n";
    cout << "  Число x = " << a << b << c << endl;
    cout << "  Зачеркиваем вторую цифру: остается " << (10 * a + c) << endl;
    cout << "  Приписываем справа вторую цифру " << b << ": получаем " << n << endl;

    return 0;
}