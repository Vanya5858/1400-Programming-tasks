#include <iostream>
using namespace std;

int main() {
    setlocale(LC_ALL, "RU");
    // Мы знаем, что после всех операций получилось 654
    int result = 654;

    // Разбираем число 654
    int c = result / 100;           // последняя цифра исходного числа (стала первой)
    int remainder = result % 100;   // оставшаяся часть

    // Из remainder = 10b + a находим a и b
    int b = remainder / 10;         // вторая цифра исходного числа
    int a = remainder % 10;         // первая цифра исходного числа

    // Восстанавливаем исходное число x
    int x = 100 * a + 10 * b + c;

    cout << "Найдено число x: " << x << endl;

    // Проверка
    cout << "Проверка:\n";
    cout << "1. Зачеркиваем последнюю цифру: из " << x << " остается " << (10 * a + b) << endl;
    cout << "2. Переставляем цифры: " << (10 * a + b) << " -> " << (10 * b + a) << endl;
    cout << "3. Приписываем слева последнюю цифру " << c << ": получаем " << result << endl;

    return 0;
}