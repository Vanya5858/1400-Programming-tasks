#include <iostream>
using namespace std;

int main() {
    setlocale(LC_ALL, "RU");
    int n;

    cout << "Введите число n (1 <= n <= 999, единицы != 0): ";
    cin >> n;

    if (n < 1 || n > 999) {
        cout << "Ошибка! Число n должно быть в диапазоне 1 ≤ n ≤ 999\n";
        return 1;
    }

    // Проверяем, что число единиц в n не равно нулю
    if (n % 10 == 0) {
        cout << "Ошибка! Число единиц в n не должно быть равно нулю\n";
        return 1;
    }

    // Для разных случаев n
    if (n < 100) {
        cout << "Ошибка! При n < 100 невозможно восстановить трехзначное число x\n";
        return 1;
    }
    else {
        int c = n / 100;           // третья цифра исходного числа x
        int remainder = n % 100;   // оставшаяся часть (10b + a)
        int b = remainder / 10;    // вторая цифра исходного числа x
        int a = remainder % 10;    // первая цифра исходного числа x

        // Восстанавливаем исходное число x
        int x = 100 * a + 10 * b + c;

        // Вывод результатов
        cout << "Для n = " << n << " найдено число x = " << x << endl;
        cout << "\nПроверка:" << endl;
        cout << "  Число x = " << a << b << c << endl;
        cout << "  Зачеркиваем последнюю цифру: остается " << (10 * a + b) << endl;
        cout << "  Переставляем цифры: " << (10 * a + b) << " -> " << (10 * b + a) << endl;
        cout << "  Приписываем слева последнюю цифру " << c << ": получаем " << n << endl;
    }

    return 0;
}