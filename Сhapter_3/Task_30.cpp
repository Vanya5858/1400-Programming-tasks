#include <iostream>
using namespace std;

int main() {
    setlocale(LC_ALL, "RU");
    int n;

    // Ввод натурального числа
    cout << "Введите натуральное число (n > 99): ";
    cin >> n;

    // Проверка, что число действительно натуральное и больше 99
    if (n <= 99) {
        cout << "Ошибка! Число должно быть больше 99\n";
        return 1;
    }

    // а) Число десятков - предпоследняя цифра числа
    int tens = (n / 10) % 10;

    // б) Число сотен - третья с конца цифра числа
    int hundreds = (n / 100) % 10;

    // Вывод результатов
    cout << "Исходное число: " << n << endl;
    cout << "а) Число десятков: " << tens << endl;
    cout << "б) Число сотен: " << hundreds << endl;

    return 0;
}