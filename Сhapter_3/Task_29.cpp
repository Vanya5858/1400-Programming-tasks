#include <iostream>
using namespace std;

int main() {
    setlocale(LC_ALL, "RU");
    int n;

    // Ввод натурального числа
    cout << "Введите натуральное число (n > 9): ";
    cin >> n;

    // Проверка, что число действительно натуральное и больше 9
    if (n <= 9) {
        cout << "Ошибка! Число должно быть больше 9\n";
        return 1;
    }

    // а) Число единиц - последняя цифра числа
    int units = n % 10;

    // б) Число десятков - предпоследняя цифра числа
    int tens = (n / 10) % 10;

    // Вывод результатов
    cout << "Исходное число: " << n << endl;
    cout << "а) Число единиц: " << units << endl;
    cout << "б) Число десятков: " << tens << endl;

    return 0;
}